def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument(
        "--data-dir",
        type=Path,
        default=None,
        help="Directory with CSV outputs (default: <repo>/ranking/output/pubmed_baseline_gwas)",
    )
    ap.add_argument(
        "--top-volume",
        type=int,
        default=40,
        help="Rows for overall volume table",
    )
    ap.add_argument(
        "--top-share",
        type=int,
        default=40,
        help="Rows for overall share table",
    )
    ap.add_argument(
        "--top-per-year",
        type=int,
        default=20,
        help="Rows per year for by-year tables",
    )
    ap.add_argument(
        "--stub",
        action="store_true",
        help="Write placeholder PubMed page + hub only (no CSV required)",
    )
    ap.add_argument(
        "--dashboard-table-limit",
        type=int,
        default=250,
        help="Max rows per ranking table in the interactive dashboard",
    )
    ap.add_argument(
        "--dashboard-bar-top",
        type=int,
        default=35,
        help="Number of journals in the horizontal bar chart",
    )
    args = ap.parse_args()
    root = _repo_root()
    data_dir = (
        args.data_dir.expanduser().resolve()
        if args.data_dir
        else root / "ranking/output/pubmed_baseline_gwas"
    )
    out_path = root / "docs/Trending/Trending_PubMed_GWAS.md"
    hub_path = root / "docs/Trending/index.md"

    dash_path = root / "docs" / DASHBOARD_ASSET_RELPATH

    out_path.parent.mkdir(parents=True, exist_ok=True)

    if args.stub:
        write_stub_dashboard_html(dash_path)
        print(f"Wrote stub {dash_path}", file=sys.stderr)
        write_stub_trending_pubmed(out_path)
        print(f"Wrote stub {out_path}", file=sys.stderr)
        write_trending_hub_md(hub_path, ICON_TRENDING_HUB_PUBMED)
        print(f"Wrote {hub_path}", file=sys.stderr)
        return 0

    if not (data_dir / "gwas_trends_by_year.csv").is_file():
        print(f"Missing CSV under {data_dir}; run pubmed_baseline_gwas.py or use --stub.", file=sys.stderr)
        return 1

    meta = _read_meta(data_dir)
    trends = _load_csv(data_dir / "gwas_trends_by_year.csv")
    vol = _load_csv(data_dir / "journal_gwas_ranking.csv")
    share = _load_csv(data_dir / "journal_gwas_ranking_by_share.csv")
    vol_y = _load_csv(data_dir / "journal_gwas_ranking_by_year.csv")
    share_y = _load_csv(data_dir / "journal_gwas_ranking_by_share_by_year.csv")

    dash_payload = _build_dashboard_payload(
        trends,
        vol,
        share,
        table_limit=max(1, args.dashboard_table_limit),
        bar_top_n=max(5, args.dashboard_bar_top),
    )
    write_interactive_dashboard_html(dash_path, dash_payload)
    print(f"Wrote {dash_path}", file=sys.stderr)

    ym, yx = meta.get("year_min", "?"), meta.get("year_max", "?")
    n_shards = meta.get("n_baseline_files", "?")
    n_j = meta.get("n_journals_any", "?")

    lines: list[str] = [
        "---",
        "hide:",
        "- tags",
        "tags:",
        "- Statistics",
        "- GWAS",
        "class: catalog-section-Trending",
        "---",
        "",
        '<div class="catalog-nav-lead" markdown="block">',
        "",
        f"**PubMed baseline** snapshot: indexed article counts for GWAS-related **MeSH** and **title/abstract** buckets, plus journal rankings (volume and share), for publication years **{ym}–{yx}**.",
        "",
        "</div>",
        "",
        "## Provenance",
        "",
        f"- **Baseline files merged:** {n_shards}",
        f"- **Distinct journals (any row):** {n_j}",
        "- **Share ranking** includes only journals with at least one GWAS-related hit and ≥ 50 articles in the window (overall) or in that calendar year (by-year tables).",
        "- Regenerate this page after re-running `scripts/pubmed_baseline_gwas.py` and `scripts/render_trending_pubmed_gwas.py`.",
        "- Interactive assets: `docs/assets/trending/pubmed_gwas_dashboard.html` (regenerated with this script).",
        "",
        _dashboard_iframe_md(),
        "## Static tables (export / print)",
        "",
        "The same numbers as in the dashboard, as plain sortable tables on this page.",
        "",
        "## Term trends by year",
        "",
        "*MeSH_GWAS* = MeSH descriptor for genome-wide association; *text_* buckets match title, abstract, or author keywords.",
        "",
        "*Click a column header to sort.*",
        "",
        _trends_table(trends),
        "",
        "## Journals — by GWAS-related volume (full window)",
        "",
        f"*Top {args.top_volume} by number of GWAS-related articles (MeSH or core text match).*",
        "",
        "*Click a column header to sort.*",
        "",
        _journal_volume_table(vol, args.top_volume),
        "",
        "## Journals — by GWAS share (full window)",
        "",
        f"*Top {args.top_share} by fraction of articles that are GWAS-related (among journals with ≥ 50 articles and ≥ 1 GWAS hit).*",
        "",
        "*Click a column header to sort.*",
        "",
        _journal_share_table(share, args.top_share),
        "",
    ]

    by_vol = _group_by_year(vol_y, "year")
    by_sh = _group_by_year(share_y, "year")
    years = sorted(set(by_vol) | set(by_sh), key=int)

    lines += [
        "## By calendar year",
        "",
        f"*Within each year: top {args.top_per_year} by volume, then by share (same eligibility as above).*",
        "",
    ]

    for y in years:
        vrows = sorted(
            by_vol.get(y, []),
            key=lambda r: int(r.get("rank_volume", "0")),
        )[: args.top_per_year]
        srows = sorted(
            by_sh.get(y, []),
            key=lambda r: int(r.get("rank_share", "0")),
        )[: args.top_per_year]
        lines += [
            f"### {y}",
            "",
            "#### Volume (GWAS-related hits)",
            "",
            "*Click a column header to sort.*",
            "",
            _journal_volume_table(vrows, args.top_per_year),
            "",
            "#### Share (among qualifying journals)",
            "",
            "*Click a column header to sort.*",
            "",
            _journal_share_table(srows, args.top_per_year),
            "",
        ]

    lines.append(_methods_section_md())

    out_path.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {out_path}", file=sys.stderr)

    write_trending_hub_md(hub_path, ICON_TRENDING_HUB_PUBMED)
    print(f"Wrote {hub_path}", file=sys.stderr)

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
