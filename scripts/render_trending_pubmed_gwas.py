#!/usr/bin/env python3
"""
Regenerate Trending docs from ranking/output/pubmed_baseline_gwas/*.csv.

Writes ``docs/Trending/Trending_PubMed_GWAS.md`` (static tables + iframe), ``docs/Trending/index.md`` (hub),
and ``docs/assets/trending/pubmed_gwas_dashboard.html`` (Plotly + Tabulator: trends + rankings).
The home-page Trending card is generated by ``src/process_homepage.py`` when you
run ``main.py``. Homepage Trending card uses 📑 in ``process_homepage.py``; hub sub-card uses ``ICON_TRENDING_HUB_PUBMED`` below.

    python3 scripts/render_trending_pubmed_gwas.py
    python3 scripts/render_trending_pubmed_gwas.py --stub   # placeholder tables page (no CSV)
"""

from __future__ import annotations

import argparse
import csv
import html
import json
import sys
from collections import defaultdict
from pathlib import Path

# Hub card on docs/Trending/index.md (avoid 📉); homepage uses 📑 in process_homepage.py.
ICON_TRENDING_HUB_PUBMED = "📋"

DASHBOARD_IFRAME_HEIGHT = 1320
DASHBOARD_ASSET_RELPATH = "assets/trending/pubmed_gwas_dashboard.html"


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[1]


def _read_meta(data_dir: Path) -> dict:
    p = data_dir / "run_meta.json"
    if p.is_file():
        return json.loads(p.read_text(encoding="utf-8"))
    return {}


def _table_wrap(inner: str) -> str:
    return (
        '<div class="catalog-summary-table" markdown="block">\n\n'
        f"{inner}\n"
        "</div>\n"
    )


def _trends_table(rows: list[dict[str, str]]) -> str:
    lines = [
        "<table>",
        "<thead><tr><th>Year</th><th>Bucket</th><th>Articles</th></tr></thead>",
        "<tbody>",
    ]
    for r in rows:
        lines.append(
            "<tr>"
            f"<td>{html.escape(r['year'])}</td>"
            f"<td>{html.escape(r['bucket'])}</td>"
            f"<td>{html.escape(r['n_articles'])}</td>"
            "</tr>"
        )
    lines.append("</tbody></table>")
    return _table_wrap("\n".join(lines))


def _journal_volume_table(rows: list[dict[str, str]], max_rows: int) -> str:
    lines = [
        "<table>",
        "<thead><tr>"
        "<th>Rank</th><th>Journal</th><th>GWAS hits</th><th>Total</th><th>Fraction</th>"
        "</tr></thead>",
        "<tbody>",
    ]
    for r in rows[:max_rows]:
        lines.append(
            "<tr>"
            f"<td>{html.escape(r['rank_volume'])}</td>"
            f"<td title=\"{html.escape(r['full_title'], quote=True)}\">"
            f"{html.escape(r['journal_key'])}</td>"
            f"<td>{html.escape(r['gwas_hits'])}</td>"
            f"<td>{html.escape(r['total_articles'])}</td>"
            f"<td>{html.escape(r['fraction_gwas'])}</td>"
            "</tr>"
        )
    lines.append("</tbody></table>")
    return _table_wrap("\n".join(lines))


def _journal_share_table(rows: list[dict[str, str]], max_rows: int) -> str:
    lines = [
        "<table>",
        "<thead><tr>"
        "<th>Rank</th><th>Journal</th><th>GWAS hits</th><th>Total</th><th>Fraction</th>"
        "</tr></thead>",
        "<tbody>",
    ]
    for r in rows[:max_rows]:
        lines.append(
            "<tr>"
            f"<td>{html.escape(r['rank_share'])}</td>"
            f"<td title=\"{html.escape(r['full_title'], quote=True)}\">"
            f"{html.escape(r['journal_key'])}</td>"
            f"<td>{html.escape(r['gwas_hits'])}</td>"
            f"<td>{html.escape(r['total_articles'])}</td>"
            f"<td>{html.escape(r['fraction_gwas'])}</td>"
            "</tr>"
        )
    lines.append("</tbody></table>")
    return _table_wrap("\n".join(lines))


def _load_csv(path: Path) -> list[dict[str, str]]:
    if not path.is_file():
        return []
    with path.open(newline="", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def _group_by_year(rows: list[dict[str, str]], year_key: str) -> dict[str, list[dict[str, str]]]:
    g: dict[str, list[dict[str, str]]] = defaultdict(list)
    for r in rows:
        g[r[year_key]].append(r)
    return dict(g)


def _json_for_script(payload: dict) -> str:
    s = json.dumps(payload, ensure_ascii=False, separators=(",", ":"))
    return s.replace("<", "\\u003c")


def _journal_payload_rows(
    rows: list[dict[str, str]], rank_field: str, limit: int
) -> list[dict]:
    out: list[dict] = []
    for r in rows[:limit]:
        fr = (r.get("fraction_gwas") or "").strip() or "0"
        try:
            f = float(fr)
        except ValueError:
            f = 0.0
        out.append(
            {
                "rank": int(r[rank_field]),
                "journal_key": r["journal_key"],
                "full_title": r.get("full_title", r["journal_key"]),
                "gwas_hits": int(r["gwas_hits"]),
                "total_articles": int(r["total_articles"]),
                "fraction_gwas": round(f, 6),
            }
        )
    return out


def _build_dashboard_payload(
    trends_rows: list[dict[str, str]],
    vol_rows: list[dict[str, str]],
    share_rows: list[dict[str, str]],
    *,
    table_limit: int,
    bar_top_n: int,
) -> dict:
    trends_json = [
        {"year": int(r["year"]), "bucket": r["bucket"], "n_articles": int(r["n_articles"])}
        for r in trends_rows
    ]
    vol_lim = _journal_payload_rows(vol_rows, "rank_volume", table_limit)
    share_lim = _journal_payload_rows(share_rows, "rank_share", table_limit)
    bar_slice = vol_rows[:bar_top_n]
    bar_json = [
        {
            "journal_key": r["journal_key"],
            "gwas_hits": int(r["gwas_hits"]),
            "full_title": r.get("full_title", ""),
        }
        for r in bar_slice
    ]
    return {
        "trends": trends_json,
        "journal_volume": vol_lim,
        "journal_share": share_lim,
        "journal_volume_bar": bar_json,
    }


