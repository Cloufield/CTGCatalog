def _methods_section_md() -> str:
    return "\n".join(
        [
            "## Methods",
            "",
            "Counts are derived offline from **NCBI PubMed baseline** Medline XML (gzip shards such as `pubmed*.xml.gz`), streamed with `xml.etree.ElementTree.iterparse` so memory stays bounded; optional workers process files in parallel and results are merged from per-file JSON shards.",
            "",
            "**Publication year** comes from `JournalIssue.PubDate` (or `ArticleDate` when needed). **Journal** is keyed by `MedlineJournalInfo.MedlineTA` when present, otherwise ISO abbreviation or full journal title.",
            "",
            "**Trend buckets (per year):** *MeSH_GWAS* counts articles whose MeSH descriptor name contains “genome-wide association”. *text_GWAS_core*, *text_PRS*, and *text_MR* count articles whose **title, abstract, and author keywords** match fixed case-insensitive patterns (e.g. “GWAS”, “genome-wide association”, polygenic risk/score, Mendelian randomization). An article can contribute to more than one text bucket.",
            "",
            "**Journal GWAS-related counts** use a single article-level flag: MeSH_GWAS **or** *text_GWAS_core* only (PRS/MR text alone does not count toward journal volume/share). **Share** rankings restrict to journals with at least one such hit and a minimum article count in the window (overall or per calendar year, as noted above).",
            "",
            "Implementation: `scripts/pubmed_baseline_gwas.py`; tables and dashboard: `scripts/render_trending_pubmed_gwas.py`.",
            "",
        ]
    )


